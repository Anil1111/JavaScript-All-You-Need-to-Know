// ********** Null vs Undefined **********

// undefined
// A Variable without Value Always Store Undefined

// null
// A Variable with Unknown Value can Store Null

var abc;
var xyz = null;

console.log(abc);
// expected output: undefined

console.log(xyz);
// expected output: null

// Null is a Special Type of Data Used as an Absence of an Object